#include "libft.h"

int	ft_putstr_mod(char *c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (c[i] != '\0')
		i++;
	i--;
	while (i >= 0)
	{
		i -= ft_putchar(c[i]);
		j++;
	}
	return (j);
}
